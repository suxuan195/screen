/* 解析页面 HTML 表格为 JSON 数据 */
export function getTables() {
    var gridArr = [];
    var obj = document.querySelector(".pop_table");
    var rows = obj.rows;
    for (var i = 1; i < rows.length; i++) { //行
        var griditem = {
            name: '',
            mainProducts: '',
            no_bAveragePrice: '',
            no_sAveragePrice: '',
            no_QOQ: '',
            no_yearAveragePrice: '',
            no_lastYearAveragePrice: '',
            no_YOY: "",
            contain_bAveragePrice: '',
            contain_sAveragePrice: "",
            contain_QOQ: '',
            contain_yearAveragePrice: '',
            contain_lastYearAveragePrice: '',
            contain_YOY: ''
        };
        for (var j = 0; j < rows[i].cells.length; j++) {
            Object.keys(griditem).forEach((item, index) => {
                if (index === j) {
                    griditem[item] = rows[i].cells[j].innerHTML
                }
            })
        }
        gridArr.push(griditem);
    }
    console.log(gridArr);
}



